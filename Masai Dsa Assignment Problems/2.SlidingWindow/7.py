# Count triplets
# Description
# You are given a sorted array consisting of N integers. You need to count to the number of triplets such that the absolute difference between the maximum and minimum element of t he triplet should be less than or equal to K.
# Note:
# A triplet is a group of three integers (a[i], a[j],a[k]) where i < j < k.
# Input
# The first line contains 2 space-separated positive integers N and K denoting the number of elements present in the array and the maximum allowed difference.
# The second line contains N space-separated positive integers A[i] denoting the elements of the array.
# Constraints
# 1 <= N <= 10^5
# 0 <= K <= 10^9
# 1 <= A[i] <= 10^9

# 4 2
# -3 -2 -1 0
# output: 2

# 4 3
# 1 2 3 4
# output:4